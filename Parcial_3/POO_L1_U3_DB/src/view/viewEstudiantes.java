    private void bottAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottAgregarActionPerformed
        String SQL = "INSERT INTO estudiantes(estuCedula, estuNombre, estuApellido, estuAsignatura, estuPromedio, estuEstado)values(?,?,?,?,?,?)";
        try {
            int select = CBEstado.getSelectedIndex();
            int select1 = CBAsignatura.getSelectedIndex();
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(SQL);
            pst.setString(1,txtNombre.getText());
            pst.setString(2,txtApellido.getText());
            pst.setString(3,txtCelular.getText());
            pst.setString(4,CBAsignatura.getItemAt(select1));
            pst.setString(5,txtTitulo.getText());
            pst.setString(6,CBEstado.getItemAt(select));
            pst.execute();
        } catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_bottAgregarActionPerformed

    private void bottEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottEditarActionPerformed
        try {
            String SQL = "UPDATE horario set estuCedula = ?, estuNombre = ?, estuApellido = ?, estuAsignatura = ?, estuPromedio = ?, estuEstado = ? where id = ?";
            int fila = tablaEstudiantes.getSelectedRow();
            int select = CBEstado.getSelectedIndex();
            int select1 = CBAsignatura.getSelectedIndex();
            String dao = (String) tablaEstudiantes.getValueAt(fila, 0);
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(SQL);
            
            pst.setString(1,txtNombre.getText());
            pst.setString(2,txtApellido.getText());
            pst.setString(3, txtCelular.getText());
            pst.setString(4, CBAsignatura.getItemAt(select1));
            pst.setString(5, txtTitulo.getText());
            pst.setString(6, CBEstado.getItemAt(select)); 
            pst.setString(7, dao); 
            pst.execute();
            JOptionPane.showMessageDialog(null, "Se ha actualizado correctamente su registro");
            mostrarDatos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error en la actualizacion"+e.getMessage());
        }
    }//GEN-LAST:event_bottEditarActionPerformed

