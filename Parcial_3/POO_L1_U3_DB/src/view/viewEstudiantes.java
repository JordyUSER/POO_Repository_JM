    public void mostrarDatos(){
        String titulos[] = {"ID","Cedula", "Nombre", "Apellido", "Asignatura", "Promedio", "Estado"};
        String registro[] = new String[7];
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        String SQL = "SELECT * FROM estudiantes";
        try {
            Statement st = (Statement) con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while(rs.next()){
                registro[0] = rs.getString("id");
                registro[1] = rs.getString("estuCedula");
                registro[2] = rs.getString("estuNombre");
                registro[3] = rs.getString("estuApellido");
                registro[4] = rs.getString("estuAsignatura");
                registro[5] = rs.getString("estuPromedio");
                registro[6] = rs.getString("estuEstado");
                modelo.addRow(registro);
            }
            tablaEstudiantes.setModel(modelo);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error al mostrar los datos.");
            System.out.println(e);
        }  
    }
    
