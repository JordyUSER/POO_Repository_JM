    private void ButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEditActionPerformed
        try {
            String SQL = "UPDATE productos set prodName = ?, prodPrice = ?, prodType = ?, prodWeight = ? where id = ?";
            int fila = tableContents.getSelectedRow();
            String dao = (String) tableContents.getValueAt(fila, 0);
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(SQL);

            pst.setString(1, txtName.getText());
            pst.setString(2, txtPrice.getText());
            pst.setString(3, txtType.getText());
            pst.setString(4, txtWeight.getText());
            pst.setString(5, dao);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Se ha actualizado correctamente su registro");
            mostrarDatos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la actualizacion" + e.getMessage());
        }
    }//GEN-LAST:event_ButtonEditActionPerformed

