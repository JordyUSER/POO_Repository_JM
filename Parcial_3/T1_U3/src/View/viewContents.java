    public void mostrarDatos() {
        String titulos[] = {"ID", "Producto", "Precio", "Tipo", "Peso"};
        String registro[] = new String[5];
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        String SQL = "SELECT * FROM productos";
        try {
            Statement st = (Statement) con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while (rs.next()) {
                registro[0] = rs.getString("id");
                registro[1] = rs.getString("prodName");
                registro[2] = rs.getString("prodPrice");
                registro[3] = rs.getString("prodType");
                registro[4] = rs.getString("prodWeight");
                modelo.addRow(registro);
            }
            tableContents.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar los datos.");
            System.out.println(e);
        }

    }
    
